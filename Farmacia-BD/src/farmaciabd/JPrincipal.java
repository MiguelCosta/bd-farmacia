        String delims                   = "[ ]+";
        String produto                  = "";

        // para o variável produto ficar apenas com o id
        produto_todo                    = sel_linha;
        String[] id                     = produto_todo.split(delims);
        produto                         = id[0];

        float montanteSel               = 0;

